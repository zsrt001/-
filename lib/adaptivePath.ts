export function getAdaptiveLead(
  questionNumber: number,
  answers: AnswerMap
): string | null {
  const previousAnswer = answers[questionNumber - 1];

  if (!previousAnswer) {
    return null;
  }

  return adaptiveLeads[questionNumber]?.[previousAnswer] ?? null;
}
